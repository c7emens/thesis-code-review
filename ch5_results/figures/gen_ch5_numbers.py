#!/usr/bin/env python3
# Produce the ch5 numerical tables from the post-refactor stage3_vessel_events.
#
# Emits three LaTeX-ready snippets:
#   1. tables/eda/events_by_farm.tex   — farm × tier breakdown
#   2. tables/eda/threshold_sweep.tex  — population at thresholds 30 / 40 / 50
#   3. ch5_headlines.txt               — headline counts for prose-paragraph substitution
#
# Usage:
#   python gen_ch5_numbers.py

from pathlib import Path

import psycopg2

from pipeline_common import DB_CONFIG

OUT_DIR = Path("/mnt/d/thesis/main/tables/eda")
HEADLINES_PATH = Path("/tmp/ch5_headlines.txt")


def fetch_events_by_farm(cur) -> list[dict]:
    cur.execute("""
        SELECT project_name,
               COUNT(*) FILTER (WHERE tier = 1)                          AS t1,
               COUNT(*) FILTER (WHERE tier = 2)                          AS t2,
               COUNT(*)                                                  AS total,
               ROUND(AVG(duration_minutes) FILTER (WHERE tier = 1)::numeric, 0) AS dur_t1,
               ROUND(AVG(duration_minutes) FILTER (WHERE tier = 2)::numeric, 0) AS dur_t2
        FROM stage3_vessel_events
        WHERE EXTRACT(YEAR FROM visit_start) = 2024
        GROUP BY project_name
        ORDER BY project_name
    """)
    cols = [d[0] for d in cur.description]
    return [dict(zip(cols, r)) for r in cur.fetchall()]


def fetch_n_turbines(cur) -> dict[str, int]:
    cur.execute("""
        SELECT project_name, COUNT(*)
        FROM wind_turbines GROUP BY 1
    """)
    return dict(cur.fetchall())


def fetch_threshold_sweep(cur) -> list[dict]:
    rows = []
    for tau in (30, 40, 50):
        cur.execute("""
            SELECT COUNT(*) FILTER (WHERE score >= %s)                          AS total,
                   COUNT(*) FILTER (WHERE score >= 75)                          AS t1,
                   COUNT(*) FILTER (WHERE score >= %s AND score < 75)           AS t2,
                   ROUND(AVG(score) FILTER (WHERE score >= %s)::numeric, 1)     AS mean_score
            FROM stage3_vessel_events
            WHERE EXTRACT(YEAR FROM visit_start) = 2024
              AND score IS NOT NULL
        """, (tau, tau, tau))
        total, t1, t2, mean = cur.fetchone()
        rows.append({"tau": tau, "total": total, "t1": t1, "t2": t2, "mean": mean})
    return rows


def render_events_by_farm(rows: list[dict], turbines: dict[str, int]) -> str:
    out = ["% Auto-generated by gen_ch5_numbers.py — do not hand-edit",
           "\\begin{table}[htbp]\\centering",
           "\\caption[Vessel maintenance events by wind farm and confidence band, 2024 calibration year.]"
           "{Vessel maintenance events by wind farm and confidence band "
           "(continuous-score classifier, 2024 calibration year). The Avg dur.\\ "
           "column reports the mean visit duration in minutes for Tier 1 and Tier 2 "
           "events respectively (\\textit{Tier 1} / \\textit{Tier 2}); the 2025\\,H1 "
           "out-of-sample counterpart is given in Table~\\ref{tab:events_by_farm_2025}.}"
           "\\label{tab:events_by_farm}",
           "\\begin{tabular}{lrrrrrr}",
           "\\toprule",
           " & & & & & & \\textbf{Avg dur.\\ (min)} \\\\",
           "\\textbf{Wind Farm} & \\textbf{Turbines} & \\textbf{Tier 1} & "
           "\\textbf{Tier 2} & \\textbf{Above-thr.} & \\textbf{Emitted} & T1\\,/\\,T2 \\\\",
           "\\midrule"]
    sum_t1 = sum_t2 = sum_total = sum_turbines = 0
    for r in rows:
        farm    = r["project_name"].replace("_", " ")
        nt      = turbines.get(r["project_name"], 0)
        sum_turbines += nt
        sum_t1 += r["t1"]
        sum_t2 += r["t2"]
        sum_total += r["total"]
        above = r["t1"] + r["t2"]
        dur_t1 = int(r["dur_t1"]) if r["dur_t1"] is not None else 0
        dur_t2 = int(r["dur_t2"]) if r["dur_t2"] is not None else 0
        out.append(f"{farm} & {nt} & {r['t1']:,} & {r['t2']:,} "
                   f"& {above:,} & {r['total']:,} & {dur_t1} / {dur_t2} \\\\")
    out += ["\\midrule",
            f"\\textbf{{Total}} & {sum_turbines} & {sum_t1:,} & {sum_t2:,} "
            f"& {sum_t1 + sum_t2:,} & {sum_total:,} & --- \\\\",
            "\\bottomrule",
            "\\end{tabular}",
            "\\end{table}"]
    return "\n".join(out)


def render_threshold_sweep(rows: list[dict]) -> str:
    out = ["% Auto-generated by gen_ch5_numbers.py — do not hand-edit",
           "\\begin{table}[htbp]\\centering",
           "\\caption{Vessel-event population sensitivity to the classification "
           "threshold $\\tau_\\text{score}$ (2024 calibration year). The selected "
           "threshold of~40 sits in the middle of the sweep range. The Tier~1 "
           "($\\geq 75$) column is constant across rows by construction: $\\tau$ "
           "acts only on the lower edge of the Tier~2 band.}\\label{tab:threshold_sweep}",
           "\\begin{tabular}{lrrrr}",
           "\\toprule",
           "\\textbf{Threshold} & \\textbf{Total events} & "
           "\\textbf{Tier 1 ($\\geq 75$)} & \\textbf{Tier 2 ($\\tau\\text{--}74$)} & "
           "\\textbf{Mean score} \\\\",
           "\\midrule"]
    for r in rows:
        label = f"$\\tau = {r['tau']}$"
        if r["tau"] == 40:
            label += " \\textit{(selected)}"
        out.append(f"{label} & {r['total']:,} & {r['t1']:,} & {r['t2']:,} "
                   f"& {r['mean']} \\\\")
    out += ["\\bottomrule",
            "\\end{tabular}",
            "\\end{table}"]
    return "\n".join(out)


def render_headlines(farms: list[dict], sweep: list[dict]) -> str:
    sum_t1 = sum(r["t1"] for r in farms)
    sum_t2 = sum(r["t2"] for r in farms)
    total  = sum_t1 + sum_t2
    pct_t1 = 100 * sum_t1 / total if total else 0
    pct_t2 = 100 * sum_t2 / total if total else 0
    out = [
        "ch5 headline counts (continuous-score classifier, 2024):",
        f"  total events                  {total:,}",
        f"  Tier 1 (score >= 75)          {sum_t1:,}  ({pct_t1:.1f}%)",
        f"  Tier 2 (40 <= score < 75)     {sum_t2:,}  ({pct_t2:.1f}%)",
        "",
        "Per-farm Tier-1 ratios:",
    ]
    for r in farms:
        n  = r["t1"] + r["t2"]
        if n == 0:
            continue
        ratio = 100 * r["t1"] / n
        farm  = r["project_name"].replace("_", " ")
        out.append(f"  {farm:20s}  {ratio:5.1f}%  ({r['t1']:,} of {n:,})")
    out += ["", "Threshold sweep:"]
    for r in sweep:
        out.append(f"  tau={r['tau']:2d}  total={r['total']:6,}  "
                   f"t1={r['t1']:6,}  t2={r['t2']:6,}  mean_score={r['mean']}")
    return "\n".join(out) + "\n"


def main() -> int:
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    with psycopg2.connect(**DB_CONFIG) as conn, conn.cursor() as cur:
        farms     = fetch_events_by_farm(cur)
        turbines  = fetch_n_turbines(cur)
        sweep     = fetch_threshold_sweep(cur)

    (OUT_DIR / "events_by_farm.tex").write_text(
        render_events_by_farm(farms, turbines) + "\n")
    (OUT_DIR / "threshold_sweep.tex").write_text(
        render_threshold_sweep(sweep) + "\n")
    HEADLINES_PATH.write_text(render_headlines(farms, sweep))

    print(render_headlines(farms, sweep))
    print(f"  → {OUT_DIR / 'events_by_farm.tex'}")
    print(f"  → {OUT_DIR / 'threshold_sweep.tex'}")
    print(f"  → {HEADLINES_PATH}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
